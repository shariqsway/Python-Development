# Law of Mathematical Calculations (Order of Operations)
# 0. Parenthesis (takes priority
# 1. Exponentiation 2 **2
# 2. Multiplication or Division
# 3. Addition or Subtraction

# Division (to get floating number)
print(10 / 3)

# Division (to get integer)
print(10 // 3)

# To get remainder (modulus)
print(10 % 3)

# Exponents (Power of something)
print(10 ** 3)

# Augmented assignment
x = 10
x += 3
print(x)