for item in 'Python':
    print(item)
print("")

# List
for item in ['Mohammad', 'John', 'Ben']:
    print(item)
print("")

# Range
for item in range(10):
    print(item)
print("")

# Custom Range
for item in range(5, 10):
    print(item)
print("")

# Custom Step Range
for item in range(5, 10, 2):
    print(item)
print("")